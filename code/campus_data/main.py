def convert_distance(distance):
    if "km" in distance:
        return float(distance.split(" km")[0].replace(",", ".")) * 1000
    else:
        return int(distance.split(" m")[0])


def format_time(time):
    return str(int(time // 1)) + " min " + str(int((time % 1) * 60)) + " s"


